package sn.ept.calcul;

public class CalculMetier {
	
	/**
	 * Somme de a et b donnés en paramètre
	 * @param a
	 * @param b
	 * @return a+b
	 */
	public double somme(double a,double b){
			return (a+b);
	}
	
	/**
	 * Produit de a et b donnés en paramètre
	 * @param a
	 * @param b
	 * @return a*b
	 */
	public double produit(double a,double b){
			return a*b;
	}

}
